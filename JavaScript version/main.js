var toggleButton = document.createElement("button");
toggleButton.innerHTML = "Open Font Changer";
toggleButton.classList.add("toggle-button");
document.body.appendChild(toggleButton);

toggleButton.addEventListener("click", function() {
    var fontChanger = document.querySelector(".font-changer-container");
    if (fontChanger.style.display === "none") {
        fontChanger.style.display = "block";
    } else {
        fontChanger.style.display = "none";
    }
});

document.getElementById("apply-font-button").addEventListener("click", changeFont);

function changeFont() {
    var fontFamily = document.getElementById("font-family").value;
    var fontSize = document.getElementById("font-size").value + "px";
    var sampleText = document.getElementById("sample-text");
    sampleText.style.fontFamily = fontFamily;
    sampleText.style.fontSize = fontSize;

    var e = document.querySelectorAll('*');
    e.forEach(function(t) {
        t.style.fontFamily = fontFamily;
    });
}
